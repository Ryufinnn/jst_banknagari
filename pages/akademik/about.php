Bank Nagari adalah satu-satunya bank daerah yang berguna untuk meningkatkan perekonomian masyarakat khususnya di Sumatera Barat. Bank Nagari berpusat di kota Padang. Bank Nagari didirikan pada tanggal 12 Maret 1962 dengan nama PT Bank Pembangunan Daerah Sumatera Barat (PT BPD Sumbar). Pendirian tersebut dipelopori oleh pemerintah daerah beserta tokoh masyarakat dan tokoh pengusaha swasta di Sumatera Barat atas dasar pemikiran perlunya suatu lembaga keuangan yang berbentuk Bank, yang secara khusus membantu pemerintah dalam melaksanakan pembangunan di daerah. 